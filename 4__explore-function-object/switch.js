var color = 'black';
if(color == 'red'){
console.log('color is red');
}
else if (color == 'blue'){
console.log('color is blue');
}
else{
console.log('color not match');
}


switch(color){
case 'blue':
console.log('color is blue');
break;
case 'red':
console.log('color is red');
break;
case 'white':
console.log('color is white');
break;
case 'green':
console.log('color is green');
break;
case 'yellow':
console.log('color is yellow');
break;
default:
console.log('color is black');

}